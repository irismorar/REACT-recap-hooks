  // eslint-disable-next-line no-unused-vars
  const findDuplicatedWords = () => {
    const duplicatedWords = [];
    for (let i = 0; i < words.length - 2; i++) {
      for (let j = i + 1; j < words.length - 1; j++) {
        if (words[i] === words[j]) {
          duplicatedWords.push(words[i]);
        }
      }
    }
    return duplicatedWords;
  };

  return {
    page,
  };
